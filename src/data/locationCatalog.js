export function getProvinceById(provinceId) {
  return LOCATION_PROVINCES.find((province) => province.id === provinceId) || null;
}

export function getCitiesForProvince(provinceId) {
  return getProvinceById(provinceId)?.cities || [];
}

export function getLocationSelection(value) {
  if (!value || typeof value !== "object") {
    return null;
  }

  const province = getProvinceById(value.provinceId);
  const city = province?.cities.find((entry) => entry.id === value.cityId) || null;

  if (!province || !city) {
    return null;
  }

  return {
    provinceId: province.id,
    province: province.label,
    cityId: city.id,
    city: city.label,
    latitude: city.latitude,
    longitude: city.longitude,
    timezone: city.timezone || "Asia/Shanghai",
    label: `${province.label} · ${city.label}`,
  };
}
